# Chain verification
# ---------------------------------------------------------------------------


def verify_chain(path: str | Path) -> list[str]:
    """Read *path* and return a list of error strings.

    An empty list means the chain is intact.  Errors include:
    - Corrupted / unparseable JSON lines.
    - A record whose ``prev`` does not match the SHA-256 of the previous line.
    - Missing ``prev`` field.
    """
    p = Path(path)
    if not p.exists():
        return []

    errors: list[str] = []
    prev_hash: str | None = None  # None means we haven't seen any record yet.

    with p.open("r", encoding="utf-8") as fh:
        for line_no, raw_line in enumerate(fh, start=1):
            stripped = raw_line.rstrip("\n")
            if not stripped.strip():
                continue  # Skip blank lines.

            # Parse JSON.
            try:
                record = json.loads(stripped)
            except json.JSONDecodeError as exc:
                errors.append(f"Line {line_no}: invalid JSON — {exc}")
                prev_hash = _sha256_line(stripped)
                continue

            # Check prev field.
            expected_prev = prev_hash if prev_hash is not None else _GENESIS_PREV
            actual_prev = record.get("prev")

            if actual_prev is None:
                errors.append(f"Line {line_no}: missing 'prev' field in record type={record.get('type')!r}")
            elif actual_prev != expected_prev:
                errors.append(
                    f"Line {line_no}: chain broken — expected prev={expected_prev!r}, "
                    f"got {actual_prev!r} (type={record.get('type')!r})"
                )

            prev_hash = _sha256_line(stripped)

    return errors
